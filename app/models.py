class Source:
    '''
    Source class to define Souurce Objects
    '''

    def __init__(self,id,name,description,url,category):

      '''
    Source class to define Source Objects
  
    Args: 
			1. id
			2. name
			3. description
			4. url
			5. category
    '''  
      self.id = id
      self.name = name
      self.description = description
      self.url = url
      self.category = category
  
class Article:
    def __init__(self,author,title,description,url,image,date):

      '''
    Article class to define Article Objects
  
    Args: 
			1. author
			2. title
			3. description
			4. url
			5. image
			6. date
    '''  
      self.author = id
      self.title = title
      self.description = description
      self.url = url
      self.image = image
      self.date = date
  
            
